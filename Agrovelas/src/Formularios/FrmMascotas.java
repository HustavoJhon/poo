    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            int opc = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar esta mascota?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (opc == JOptionPane.YES_OPTION) {
                try {
                    Connection con = new Conectar().getConexion();
                    String sql = "DELETE FROM TMascota WHERE Id=?";
                    PreparedStatement pst = con.prepareStatement(sql);
                    pst.setInt(1, Integer.parseInt(TxtCodigo.getText()));
                    int n = pst.executeUpdate();
                    if (n > 0) {
                        JOptionPane.showMessageDialog(this, "Mascota eliminada correctamente");
                        limpiar();
                        vaciarTabla();
                        verDatos();
                    }
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(this, "Error al eliminar mascota", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } // TODO add your handling code here:
    }//GEN-LAST:event_BtnEliminarActionPerformed

