 public void verDatos() {
       try {
        Connection con1 = new Conectar().getConexion();

        // NUEVO MODELO con cabecera definida desde cero
        String[] titulos = {"ID", "Nombre", "Edad", "Raza", "Especie", "Peso"};
        DefaultTableModel miModelo = new DefaultTableModel(null, titulos);

        String sql = "SELECT * FROM TMascota";
        Statement st = con1.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            String[] fila = new String[6];
            fila[0] = rs.getString("Id");
            fila[1] = rs.getString("nombre_mascota");
            fila[2] = rs.getString("edad");
            fila[3] = rs.getString("raza");
            fila[4] = rs.getString("especie");
            fila[5] = rs.getString("peso");
            miModelo.addRow(fila);
        }

        jTable1.setModel(miModelo);  // Ahora sí lo aplica correctamente

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al visualizar mascotas: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }
